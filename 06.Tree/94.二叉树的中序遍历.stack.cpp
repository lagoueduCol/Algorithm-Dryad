/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
   public:
    vector<int> inorderTraversal(TreeNode *root) {
        stack<TreeNode *> t;
        TreeNode dummy;
        dummy.right = root;
        root = &dummy;

        vector<int> ans;

        while (!t.empty() || (root && root->right)) {
            root = root ? root->right : nullptr;
            while (root) {
                t.push(root);
                root = root->left;
            }

            root = t.top();
            t.pop();

            ans.push_back(root->val);
        }
        return ans;
    }
};
